// using the + operator
const message1 =
  "This is a long message " +
  "that spans across multiple lines" +
  "in the code.";

// using the \ operator
const message2 =
  "This is a long message \
that spans across multiple lines \
in the code.";

// using the template literal
const message3 = `This is a long message
that spans across multiple lines
in the code.`;


console.log(message1);
console.log(message2);
console.log(message3);

