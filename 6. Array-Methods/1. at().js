// Array At() Method
// It accepts the index number and gives the element
// of that specific index
const arr = ["Java", "C", "C++", "Python", "Javascript"];

console.log(arr.at(0));
//              Index ==> Element (Java)
console.log(arr.at(1));
console.log(arr.at(2));
console.log(arr.at(3));
console.log(arr.at(4));
console.log(arr.at(5)); // if there is no index
//  then it will show undefined

// Index ===> element

// Note: It only returns a element of a specific index
