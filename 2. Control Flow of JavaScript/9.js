// Loops :- Loops basicallu means the iteration when you want to excute a Statement repeadtly multiple times then we called it
// a Iteration

// There are 2 Types of Loops
// 1.Entry Conrtrolled Loops
// if there is a condition to stop the loop at the starting of the loop and the condition is checked at the starting of the loop
// then we called it a Entry Controlled Loop

// 2.Exit Controlled Loops
// if the condition is checked at the end of the loop then we called it a Exit Controlled Loop

// 1.Entry Conrtrolled Loops are
//  1.1--- for loop
// 1.2 --- while loop

// FOR Loop
// for is a keyword
// for is a entry controlled loop
// for is a loop which is used to iterate the code multiple times

// for (initialization; condition; increment/decrement) {
//     // code block to be executed
//   }

// for (let i = 0; i < 10; i++) {
//     console.log(i);
//   }

